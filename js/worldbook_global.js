// ========== 构建全局世界书 prompt ==========
function buildGlobalWorldbookPrompt(isDungeon) {
  var entries = GLOBAL_WORLDBOOK.slice();

  // 按 order 降序排列（高优先）
  entries.sort(function(a, b) { return b.order - a.order; });

  var prompt = '';
  prompt += '═══════════════════════════════════\n';
  prompt += '【世界书 — 苍白回廊】\n';
  prompt += '═══════════════════════════════════\n\n';

  entries.forEach(function(entry) {
    // 副本专用条目在大世界中跳过部分内容
    if (!isDungeon) {
      // 大世界中不需要副本指令协议、钥匙机制等
      if (entry.name === 'AI指令协议' || entry.name === '钥匙锁门机制' || entry.name === '恐怖事件分支') {
        return;
      }
      // 大世界中不需要副本交互规则和NPC行为
      if (entry.name === '副本交互规则' || entry.name === '副本NPC行为' || entry.name === '副本归来过渡') {
        return;
      }
    }

    prompt += '【' + entry.name + '】\n';
    prompt += entry.content + '\n\n';
  });

  prompt += '═══════════════════════════════════\n';
  prompt += '【以上为世界书内容，AI必须遵守但不能直接告诉玩家】\n';
  prompt += '═══════════════════════════════════\n\n';

  return prompt;
}
