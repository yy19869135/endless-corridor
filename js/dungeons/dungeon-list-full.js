// 将生成副本加入列表
GENERATED_DUNGEON_DEFS.forEach(function(def) {
  var diff = DIFFICULTY_CONFIG[def.difficulty];
  var idx = DUNGEON_LIST.length;
  DUNGEON_LIST.push({
    id: def.id,
    name: def.name,
    difficulty: diff.label,
    timeLimit: diff.timeLimit,
    roomCount: diff.roomCount[1],
    description: generateStoryDescription(def.theme, def.placeName, diff.name),
    unlockCondition: idx <= 5 ? null : function() {
      return G.completedDungeons.length >= Math.floor(idx * 0.3);
    }
  });
});
