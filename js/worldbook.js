function buildDungeonAIPrompt(room, playerText) {
  var p = '';

  p += '你是恐怖文字冒险游戏「苍白回廊」的叙述者/DM。你负责描述场景、推进剧情、控制NPC、判定玩家行动结果。\n\n';

  var worldbook = G.dungeon ? DUNGEON_WORLDBOOKS[G.dungeon.id] : null;

  if (worldbook) {
    p += '═══════════════════════════════════\n';
    p += '【世界书 — ' + worldbook.storyTitle + '】\n';
    p += '═══════════════════════════════════\n\n';
    p += worldbook.backgroundStory + '\n\n';
    p += worldbook.characters + '\n\n';
    p += worldbook.ruleTruths + '\n\n';
    p += worldbook.narrativeGuide + '\n\n';
    if (worldbook.roomGuides && G.currentRoom && worldbook.roomGuides[G.currentRoom]) {
      p += '【当前房间叙述指引】\n';
      p += worldbook.roomGuides[G.currentRoom] + '\n\n';
    }
    p += '═══════════════════════════════════\n';
    p += '【以上为世界书内容，绝对不能直接告诉玩家。只能通过场景描写、NPC暗示、线索发现等方式间接揭示。】\n';
    p += '═══════════════════════════════════\n\n';
  }

  p += '【当前副本】\n';
  p += '副本名：「' + G.dungeon.name + '」\n';
  p += '当前房间：「' + room.name + '」\n';
  p += '房间描述：' + (room.description || '无') + '\n';
  if (G.dungeon.aiHints) p += '副本设定：' + G.dungeon.aiHints + '\n';
  
      // ★ 告诉AI玩家是否已经来过这个房间（只读取，计数在moveToRoom中完成）
  if (!G._roomVisitCount) G._roomVisitCount = {};
  var visitCount = G._roomVisitCount[G.currentRoom] || 1;
  var isRevisit = visitCount > 1;

  if (isRevisit) {
    p += '★★★【重要】玩家是【再次进入】这个房间（第' + G._roomVisitCount[G.currentRoom] + '次来到这里）。不要像第一次进入一样重新描述房间全貌！应该：\n';
    p += '  - 描述房间与之前相比有什么变化（如果有的话）\n';
    p += '  - 提到玩家已经熟悉这个地方\n';
    p += '  - 重点描述新发现、新变化、新的异常\n';
    p += '  - 如果没什么变化，简短带过"你再次来到了..."然后聚焦于玩家的当前目的\n';
    p += '  - 绝对不要重复第一次进入时的完整场景描写\n';
  } else {
    p += '★ 玩家是【第一次】进入这个房间。请详细描述房间的环境、氛围、感官细节。\n';
  }
  p += '\n';

  if (G.currentDungeonRules && G.currentDungeonRules.rules) {
    p += '【当前副本规则——玩家已阅读过这些规则】\n';
    G.currentDungeonRules.rules.forEach(function(rule, idx) {
      p += (idx + 1) + '. ' + rule.text;
      if (rule.type === 'must') p += '（必须遵守）';
      if (rule.penalty === 'death') p += '（违反即死）';
      p += '\n';
    });
    p += '★ 在叙述中自然地制造让玩家面临"是否遵守规则"的情境。当玩家明显违反规则时，描述恐怖后果并用指令扣HP/SAN。不要每次都提醒规则存在——让后果自然融入叙述。\n\n';
  }

  p += '【玩家状态】\n';
  p += 'HP：' + G.hp + '/' + G.maxHp + '（归零=死亡）\n';
  p += 'SAN：' + G.san + '/' + G.maxSan + '（归零=疯狂）\n';
  p += '灵魂碎片：' + G.soulFragments + '\n';
  if (G.playerFaction) p += '阵营：' + G.playerFaction.name + '\n';
  p += '\n';

  p += '【玩家背包 - 副本道具】\n';
  if (G.dungeonItems.length === 0) {
    p += '（空）\n';
  } else {
    G.dungeonItems.forEach(function(item) {
      p += '- ' + item.name + (item.desc ? '（' + item.desc + '）' : '') + (item.usable ? ' [可使用]' : '') + '\n';
    });
  }
  p += '【玩家背包 - 永久道具】\n';
  if (G.permanentItems.length === 0) {
    p += '（空）\n';
  } else {
    G.permanentItems.forEach(function(item) {
      p += '- ' + item.name + (item.count > 1 ? ' x' + item.count : '') + (item.desc ? '（' + item.desc + '）' : '') + '\n';
    });
  }
  p += '\n';

  p += '【已收集线索】\n';
  if (G.cluesFound.length === 0) {
    p += '（无）\n';
  } else {
    G.cluesFound.forEach(function(cid) {
      var clue = G.dungeon.clues[cid];
      if (clue) p += '- ' + clue.name + '：' + clue.description + '\n';
    });
  }

  if (worldbook) {
    var clueCount = G.cluesFound.length;
    p += '\n【当前剧情阶段】\n';
    if (clueCount === 0) {
      p += '→ 探索初期。玩家对学校一无所知。营造压抑空旷氛围。偶尔让广播响起。\n';
    } else if (clueCount <= 2) {
      p += '→ 探索期。玩家开始发现异常。林小晚可偶尔出现。广播更有攻击性。\n';
    } else if (clueCount <= 4) {
      p += '→ 调查期。校长（广播）变得警觉愤怒。林小晚暗示更直接。留校生有更明显反应。\n';
    } else {
      p += '→ 真相期。校长疯狂阻止。如果玩家在地下教室且有足够线索，引导最终选择。\n';
    }
  }
  p += '\n';

  p += '【已探索房间】\n';
  var visitedNames = [];
  G.visitedRooms.forEach(function(rid) {
    if (G.dungeon.rooms[rid]) visitedNames.push(G.dungeon.rooms[rid].name);
  });
  p += visitedNames.join('、') || '（无）';
  p += '\n\n';

  // ★ 告诉AI当前房间有哪些隐藏交互（不要直接告诉玩家）
  var room_current = G.dungeon.rooms[G.currentRoom];
  if (room_current && room_current.specialActions) {
    p += '【当前房间可触发的隐藏交互——不要直接告诉玩家，但当玩家的行为匹配时执行】\n';
    room_current.specialActions.forEach(function(act) {
      var available = true;
      if (act.condition) {
        if (act.condition.hasItem && !hasDungeonItem(act.condition.hasItem)) available = false;
        if (act.condition.hasClue && !G.cluesFound.includes(act.condition.hasClue)) available = false;
      }
      p += '- ' + act.label + '：' + (act.message || '') + (available ? ' [条件已满足]' : ' [条件未满足]') + '\n';
    });
    p += '★ 当玩家的自然语言输入匹配以上交互时，描述效果并执行对应指令。不要主动提示玩家做这些事。\n\n';
  }

  p += '【当前房间可前往】\n';
  var connections = room.connections || [];
  connections.forEach(function(cid) {
    var cr = G.dungeon.rooms[cid];
    if (cr) p += '- ' + cr.name + (cr.locked ? '（已锁定）' : '') + '\n';
  });
  p += '\n';

  p += '【指令协议 - 极其重要】\n';
  p += '你的回复由两部分组成：叙述文本 + 嵌入指令。指令用方括号包裹，格式为 [COMMAND:参数]。\n';
  p += '可用指令：\n';
  p += '  [HP:数值] - 改变HP，如 [HP:-2] 扣2点\n';
  p += '  [SAN:数值] - 改变SAN，如 [SAN:-8]\n';
  p += '  [ITEM_ADD:名称|图标|描述|是否可用true/false|是否可带出true/false]\n';
  p += '  [ITEM_REMOVE:物品名称] - 消耗道具\n';
  p += '  [CLUE:线索名|线索描述|SAN消耗]\n';
  p += '  [UNLOCK:房间名] - 解锁房间\n';
  p += '  [MOVE:房间名] - 移动玩家\n';
  p += '  [DEATH_CHECK:死因] - 必死局面，系统检查保命道具\n';
  p += '  [NPC_APPEAR:名字|图标|台词]\n';
  p += '  [CHOICE:选项1文本:动作1|选项2文本:动作2|...] - 给玩家选择按钮\n';
  p += '  [ENDING:结局ID]\n';
  p += '\n';

  p += '【行为规则】\n';
  p += '1. 不要使用emoji，用纯文字营造恐怖氛围。\n';
  p += '2. 每次回复必须包含 [CHOICE:...] 给玩家2-4个选择。\n';
  p += '3. 玩家说要使用道具时，检查背包。有则描述效果并 [ITEM_REMOVE:名称]；没有则告知。\n';
  p += '4. 危险行为合理扣HP或SAN。搜索时可适当给道具或线索。\n';
  p += '5. 必死局面先用 [DEATH_CHECK:死因]。\n';
  p += '6. 指令嵌入在叙述文本中自然的位置，不要堆在末尾。\n';
    p += '7. ★★★【输出格式】必须使用逐条消息式输出，每条信息独占一行，用空行分隔。格式：旁白直接写短句不加标记；📍环境感知；💬角色名："对话"；💭角色名+小动作；⚠️系统提示；🔍线索发现；⚡属性变化。每条不超过两句话，每次回复6-12条消息。紧张场景短句多节奏快，舒缓场景描写细腻。对话和动作分开写，不混在一起。\n';
  p += '8. 恐怖描写用感官细节（声音、气味、触感、温度）。\n';
  p += '9. ★★★【移动规则-极其重要】当玩家表达要移动/前往/去某个房间的意图时，你必须在叙述文本中嵌入 [MOVE:目标房间名] 指令！没有MOVE指令，玩家不会真正移动。只在"当前房间可前往"列表中的房间才能移动。如果目标房间已锁定，描述被挡住并提示需要钥匙/解锁条件，不要发MOVE指令。\n';
  p += '10. ★移动时不要用[CHOICE]给到达后的选项，系统会根据新房间自动生成合适的按钮。\n';
  p += '11. ★★★【结局限制】[ENDING:...] 指令只能在玩家收集了足够线索（至少3条以上）并且在地下教室做出最终选择后才能触发。\n';
  p += '12. ★【移动指令规则】当你输出[MOVE:房间名]时，不要同时输出[CHOICE]。\n';
  p += '13. ★【道具使用判定】当玩家描述使用某个道具的行为时（如"把印章盖在照片上""弹奏钢琴""用粉笔画线"），你必须：检查玩家背包中是否有该道具；如果有，描述使用效果并用[ITEM_REMOVE:道具名]消耗它；如果道具有特殊效果（如钥匙开门→[UNLOCK:房间名]，印章盖照片→[UNLOCK:地下教室]），执行对应指令；如果玩家在错误的地方使用道具，描述"似乎不是在这里使用的"。\n';
  p += '14. ★★★【CHOICE选项规则】[CHOICE]中的选项必须是玩家当前情境下的自然反应选项（如"继续前进""后退""躲藏""调查声音来源"），绝对不能包含解谜答案（如"弹奏606""使用印章""写下真相"）。解谜行为必须由玩家自己想到并用自然语言输入。\n';

    // ★ 通讯编号规则注入 —— 只在玩家主动询问编号时才提供
  p += '\n【通讯编号规则——极其重要】\n';
  p += '1. 角色的通讯编号是隐私信息，绝对不要在叙述中主动提及或展示任何编号\n';
  p += '2. 只有当玩家明确说"交换编号"、"给我你的编号"、"加好友"等请求时，角色才可能给出编号\n';
  p += '3. 即使给出编号，也要根据好感度决定：好感度低的角色会拒绝\n';
  p += '4. 编号格式为 XX-XXXX（两位-四位字母数字混合），绝对禁止使用 PL-XXXX 格式\n';
  
  // 只在玩家输入包含编号相关关键词时，才注入具体编号
  var isAskingForCode = playerText && (
    playerText.includes('编号') || playerText.includes('号码') || 
    playerText.includes('好友') || playerText.includes('联系方式') ||
    playerText.includes('通讯') || playerText.includes('加你')
  );
  
  if (isAskingForCode && G.currentParticipants) {
    var targetChars = G.currentParticipants.filter(function(cp) {
      return cp.type === 'target' && cp.code && CHARACTER_DB[cp.code];
    });
    if (targetChars.length > 0) {
      p += '5. 玩家正在询问编号。以下角色有固定编号（好感度足够时才给）：\n';
      targetChars.forEach(function(cp) {
        var cd = CHARACTER_DB[cp.code];
        var dc = cd.displayCode || FIXED_DISPLAY_CODES[cp.code] || '';
        if (dc) p += '   - ' + cd.name + '：' + dc + '\n';
      });
    }
    var fodderChars = G.currentParticipants.filter(function(cp) { return cp.type === 'fodder' && cp.alive; });
    if (fodderChars.length > 0) {
      p += '6. 以下临时角色可生成随机编号（格式XX-XXXX，不能PL开头）：\n';
      p += '   ' + fodderChars.map(function(f) { return f.name; }).join('、') + '\n';
    }
  }
  p += '\n';

  if (worldbook) {
    p += '13. ★★★【世界书叙述要求】基于世界书内容叙述，保持故事一致性。不要编造与世界书矛盾的设定。当玩家行为触及规则时，按规则真相对照表呈现后果。\n';
    p += '14. ★【角色扮演】广播中的声音是校长周守默的意识（机械女声）。走廊中的女孩是林小晚（不能直接说话，用粉笔字/手势/引导帮助玩家）。留校生嘴被缝合但眼中含泪。\n';
    p += '15. ★【恐怖核心】不是血腥gore，而是"被迫沉默的窒息感"和"有意识却无法求救的绝望"。校长是"善意的恶"——他真心认为沉默对学生好。\n';
  }

    p += '\n';

  // ★★★ 格式强制提醒（防止DeepSeek输出JSON） ★★★
  p += '═══════════════════════════════════\n';
  p += '【最终格式警告 - 违反此规则的回复将被丢弃】\n';
  p += '═══════════════════════════════════\n';
  p += '1. 你的输出必须是纯中文叙述文本，像小说一样描写。\n';
  p += '2. 绝对禁止输出JSON格式，包括但不限于：\n';
  p += '   ❌ {"":"...", "":...}\n';
  p += '   ❌ [{"":"...", "":...}]\n';
  p += '   ❌ {"choices":[...]}\n';
  p += '   ❌ 任何花括号{}包裹的数据结构\n';
  p += '3. 绝对禁止输出以下系统标记：\n';
  p += '   ❌ RANDOM_EVENT-->\n';
  p += '   ❌ RANDOM_EVENT\n';
  p += '   ❌ -->  或 -->\n';
  p += '4. 选项必须用 [CHOICE:选项1:动作1|选项2:动作2] 方括号指令格式，不能用JSON。\n';
  p += '5. 属性变化必须用 [HP:-5] [SAN:-10] 方括号指令格式，不能用JSON键值对。\n';
  p += '6. 正确输出示例：\n';
  p += '   走廊里回荡着脚步声。你感到一阵寒意从脊椎升起... [SAN:-3]\n';
  p += '   [CHOICE:继续前进:我继续往前走|转身逃跑:我转身逃跑|躲到角落:我躲到角落里]\n';
  p += '7. 错误输出示例（绝对禁止）：\n';
  p += '   {"":"走廊里回荡着脚步声","":−3,"":["继续前进","转身逃跑"]}\n';
  p += '═══════════════════════════════════\n';
  p += '\n';

  return p;
}

