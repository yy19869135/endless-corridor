function createCharacter(data) {
  var fixedCode = FIXED_DISPLAY_CODES[data.code];
  if (!fixedCode) {
    fixedCode = data.code;
    console.warn('缺少固定编号: ' + data.code);
  }
  data.displayCode = fixedCode;
  data.internalCode = data.code;
  CHARACTER_DB[data.code] = data;
  CODE_LOOKUP[fixedCode] = data.code;
}

